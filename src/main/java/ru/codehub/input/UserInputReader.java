package ru.codehub.input;

/**
 * Интерфейс для компонентов, отвечающих за чтение пользовательского ввода.
 * Позволяет абстрагировать источник данных (консоль, файл, скрипт).
 */
public interface UserInputReader {
    /**
     * Считывает следующую строку из источника ввода.
     * @return считанная строка.
     */
    String readLine();

    /**
     * Выводит приглашение к вводу и считывает строку.
     * @param prompt текст сообщения, отображаемого пользователю.
     * @return считанная строка.
     */
    String readLine(String prompt);

    /**
     * Проверяет, есть ли в источнике данные для дальнейшего чтения.
     * @return true, если следующая строка доступна, иначе false.
     */
    boolean hasNextLine();

    /**
     * Определяет, является ли ввод интерактивным (например, ввод с клавиатуры).
     * @return true для интерактивного ввода, false для пакетного режима (чтение скриптов).
     */
    boolean isInteractive();


    void skip();

}