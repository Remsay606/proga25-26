package ru.codehub.command;

/**
 * Интерфейс для реализации команд паттерна Command.
 * Каждая команда инкапсулирует определенное действие над коллекцией или приложением.
 */
public interface Command {
    /**
     * Возвращает уникальное имя команды, по которому она вызывается пользователем.
     * @return имя команды.
     */
    String getName();

    /**
     * Возвращает описание функционала команды для вывода в справке.
     * @return краткое описание команды.
     */
    String getDescription();

    /**
     * Основной метод выполнения логики команды.
     * @param args массив строковых аргументов, переданных вместе с командой.
     */
    void execute(String[] args);

    /**
     * Определяет, является ли команда завершающей работу приложения.
     * По умолчанию возвращает false.
     * @return true, если после выполнения команды приложение должно закрыться.
     */
    default boolean isTerminating() {
        return false;
    }
}